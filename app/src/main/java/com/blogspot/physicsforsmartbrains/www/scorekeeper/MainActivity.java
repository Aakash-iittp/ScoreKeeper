    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_basketball) {
            Intent intent = new Intent(this, basketball_main.class);
            startActivity(intent);
        } else if (id == R.id.nav_badminton) {
            Intent intent = new Intent(this, badminton_main.class);
            startActivity(intent);
        } else if (id == R.id.nav_wrestling) {
            Intent intent = new Intent(this, wrestling_main.class);
            startActivity(intent);
        } else if (id == R.id.nav_tabletennis) {
            Intent intent = new Intent(this, tabletennis_main.class);
            startActivity(intent);
        } else if (id == R.id.nav_soccor) {
            Intent intent = new Intent(this, soccor_main.class);
            startActivity(intent);
        } else if (id == R.id.nav_cricket) {
            Intent intent = new Intent(this, cricket.class);
            startActivity(intent);
        } else if (id == R.id.nav_steps) {
            Intent intent = new Intent(this, steps.class);
            startActivity(intent);
        } else if (id == R.id.nav_aboutus) {
            Intent intent = new Intent(this, aboutus.class);
            startActivity(intent);
        } else if (id == R.id.nav_history) {
            Intent intent = new Intent(this, Search_history.class);
            startActivity(intent);
        } else if (id == R.id.nav_send) {
            Intent intent = new Intent(this, review.class);
            startActivity(intent);
        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }

    @Override
    public void onBackPressed() {
        Vibrator v = (Vibrator) getSystemService(Context.VIBRATOR_SERVICE);
        v.vibrate(100);
        new AlertDialog.Builder(this)
                .setIcon(android.R.drawable.ic_dialog_alert)
                .setTitle("Closing Score Keeper App")
                .setMessage("Are you sure you want to close this activity?")
                .setPositiveButton("Yes", new DialogInterface.OnClickListener() {
                    @Override
                    public void onClick(DialogInterface dialog, int which) {
                        finish();
                    }

                })
                .setNegativeButton("No", null)
                .show();

    }

    private void shareApplication() {
        ApplicationInfo app = getApplicationContext().getApplicationInfo();
        String filePath = app.sourceDir;
        Intent intent = new Intent(Intent.ACTION_SEND);
        intent.setType("*/*");
        // Append file and send Intent
        intent.putExtra(Intent.EXTRA_STREAM, Uri.fromFile(new File(filePath)));
        startActivity(Intent.createChooser(intent, "Share app via"));
    }
